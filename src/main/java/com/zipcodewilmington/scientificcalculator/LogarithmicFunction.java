package com.zipcodewilmington.scientificcalculator;

public class LogarithmicFunction {

    public static double log(double num1){
        return Math.log(num1);
        //Returns natural logarithm (base e) of a double value

    }

    public static double log10( double num1){
        return Math.log10(num1);
        //Returns the base 10 logarithm of a double value
    }

    public static double log1p(double num1){
        return Math.log1p(num1);
    }

    public static double E(){
        return Math.E;
    }


}
